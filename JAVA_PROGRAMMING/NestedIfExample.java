class NestedIfExample 
{
	public static void main(String[] args) 
	{
		int num=1;
		if (num>=0)
		{
            if(num%2==0)
			{
				System.out.println("Zinga Rocks");
			}
			else
			{
				System.out.println("Tinga Rocks");
				}
		}
		else
		{
			System.out.println("Tingi Rocks");
		}

	}
}
