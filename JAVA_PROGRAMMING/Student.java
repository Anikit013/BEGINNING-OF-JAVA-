class Student 
{
	public static void main(String[] args) 
	{
		 char grade = 'A';
		switch(grade)
		{
			case 'A':System.out.println("FCD");
			break;
			case 'B':System.out.println("FD");
			break;
			case 'C':System.out.println("SD");
			break;
			case 'D':System.out.println("Fail");
			break;
			default :System.out.println("Invalid Choice");
		}
	}
}





