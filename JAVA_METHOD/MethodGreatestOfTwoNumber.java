class MethodGreatestOfTwoNumber 
{
	public static void main(String[] args) 
	{
		int num1=10;
		int num2=20;
		check(num1,num2);
	}
	public static  void check(int num1, int num2)
	{
		if (num1>num2)
		{
			System.out.println(num1+ "is greater");

		}
		else 
		{
			System.out.println(num2+ "is greater");
		}
	}
}

